class Stack {
private:
    queue <int> q;

public:
    // Push element x onto stack.
    void push(int x) {
        q.push(x);
    }

    // Removes the element on top of the stack.
    void pop() {
        queue <int> q_new;
        while (SZ(q) > 1) {
            q_new.push(q.front());
            q.pop();
        }
        q = q_new;
    }

    // Get the top element.
    int top() {
        return q.back();
    }

    // Return whether the stack is empty.
    bool empty() {
        return q.empty();
    }
};

