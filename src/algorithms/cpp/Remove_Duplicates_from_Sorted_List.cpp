struct ListNode {
    int val;
    ListNode *next;
    ListNode(int x) : val(x), next(NULL) {}
};

class Solution {
public:
    ListNode* deleteDuplicates(ListNode* head) {
        ListNode *node = head, *pre = NULL;
        int cnt;
        while (node) {
            if (pre && pre->val == node->val) {
                pre->next = node->next;
            }
            else {
                pre = node;
            }
            node = node->next;
        }
        return head;
    }
};

